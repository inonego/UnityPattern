using System;

namespace inonego
{
    // =======================================================================================
    /// <summary>
    /// 레벨 인터페이스입니다.
    /// </summary>
    // =======================================================================================
    public interface ILevel
    {  
        // ------------------------------------------------------------
        /// <summary>
        /// 이벤트를 호출할지 여부를 결정합니다.
        /// </summary>
        // ------------------------------------------------------------
        bool InvokeEvent { get; set; }

        // ------------------------------------------------------------
        /// <summary>
        /// 현재 레벨입니다.
        /// </summary>
        // ------------------------------------------------------------
        int Current { get; set; }

        // ------------------------------------------------------------
        /// <summary>
        /// 최대로 도달할 수 있는 레벨의 값입니다.
        /// </summary>
        // ------------------------------------------------------------
        int Max { get; set; }

        // ------------------------------------------------------------
        /// <summary>
        /// 현재 레벨에서 다음 레벨로 넘어갈 수 있는지를 반환합니다.
        /// </summary>
        // ------------------------------------------------------------
        bool CanLevelUp { get; }

    #region 이벤트

        public event Action<ILevel, LevelUpEventArgs> OnLevelUp;

    #endregion

    #region 메서드

        // ------------------------------------------------------------
        /// <summary>
        /// 레벨 업 시킵니다.
        /// </summary>
        // ------------------------------------------------------------
        void LevelUp(int amount = 1);

    #endregion

    }

}